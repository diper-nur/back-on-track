$stmt = $conn->prepare("
    SELECT 
        id,
        title,
        description,
        notes,
        deadline,
        due_date,
        material_id,
        status,
        created_at
    FROM tasks
    WHERE user_id = ?
    AND status = 'pending'
    ORDER BY
        CASE
            WHEN COALESCE(NULLIF(deadline, '0000-00-00'), NULLIF(due_date, '0000-00-00')) IS NULL THEN 4
            WHEN COALESCE(NULLIF(deadline, '0000-00-00'), NULLIF(due_date, '0000-00-00')) < CURDATE() THEN 1
            WHEN COALESCE(NULLIF(deadline, '0000-00-00'), NULLIF(due_date, '0000-00-00')) = CURDATE() THEN 2
            WHEN COALESCE(NULLIF(deadline, '0000-00-00'), NULLIF(due_date, '0000-00-00')) <= DATE_ADD(CURDATE(), INTERVAL 3 DAY) THEN 3
            ELSE 4
        END,
        COALESCE(NULLIF(deadline, '0000-00-00'), NULLIF(due_date, '0000-00-00')) ASC,
        created_at ASC
");

if (!$stmt) {
    die("Prepare failed: " . $conn->error);
}

