}

function displayPriority($priority) {
    $p = strtolower(trim($priority));

    if (strpos($p, "critical") !== false || strpos($p, "крит") !== false || strpos($p, "өте") !== false) {
        return rpText("critical");
    }

    if (strpos($p, "high") !== false || strpos($p, "выс") !== false || strpos($p, "жоғ") !== false) {
        return rpText("high");
    }

    if (strpos($p, "medium") !== false || strpos($p, "сред") !== false || strpos($p, "орта") !== false) {
        return rpText("medium");
    }

    if (strpos($p, "low") !== false || strpos($p, "низ") !== false || strpos($p, "төмен") !== false) {
        return rpText("low");
    }

    return $priority;
}

