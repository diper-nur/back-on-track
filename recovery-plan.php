if (isset($_POST["generate"])) {

    $task_text = "";

    foreach ($tasks as $t) {
        $title = trim($t["title"] ?? "");

        if ($title === "") {
            continue;
        }

        $taskType = detectTaskType($t);
        $deadline = deadlineValue($t);
        $timeRange = suggestedTimeRange($taskType, $title);

        $task_text .= "- Task ID: {$t['id']}\n";
        $task_text .= "  Title: {$title}\n";
        $task_text .= "  Type: {$taskType}\n";
        $task_text .= "  Deadline: {$deadline}\n";
        $task_text .= "  Suggested time range: {$timeRange}\n";
        $task_text .= "  Notes: " . trim($t["notes"] ?? "") . "\n\n";
    }

    if (trim($task_text) === "") {
        $ai_response = rpText("no_tasks");
    } else {

        $taskHash = hash("sha256", $task_text);

        /*
           v3_multilang_pretty:
           - prevents old English cached plan from appearing
           - creates separate cache for RU/KZ/EN
        */
        $cacheKey = "recovery_plan_v3_multilang_pretty_user_" . $user_id . "_" . $taskHash . "_" . $lang;

        if (!$forceRefresh) {
            $cachedPlanJson = getAiCache($conn, $cacheKey);

            if ($cachedPlanJson !== null && trim($cachedPlanJson) !== "") {
                $cachedDecoded = json_decode($cachedPlanJson, true);

                if (
                    json_last_error() === JSON_ERROR_NONE &&
                    isset($cachedDecoded["plan"]) &&
                    is_array($cachedDecoded["plan"])
                ) {
                    $plan = $cachedDecoded["plan"];
                    $usedCache = true;
                }
            }
        }

        if (empty($plan)) {

            $outputLanguage = outputLanguageName();

            $prompt = "
You are an academic recovery planner.

The student has these pending tasks:

$task_text

Create a realistic study recovery plan for the next 3 days.

OUTPUT LANGUAGE:
- Write all values in JSON in $outputLanguage.
- Translate priority, day, type, time, and reason into $outputLanguage.
- Keep dates in YYYY-MM-DD format.

IMPORTANT TIME RULES:
- Manual tasks must usually take 1-2 hours.
- AI-generated material tasks must be short review/practice tasks.
- AI-generated material tasks should usually take 5-15 minutes.
- Do NOT assign 1 hour to a small AI-generated task.
- If an AI-generated task is simple, give 5-10 minutes.
- If an AI-generated task requires solving, calculation, analysis, comparison, evaluation, or explanation, give 10-15 minutes.
- Do not overload one day.
- Prioritize by deadline first, then importance.
- Urgent deadlines must appear earlier.
- If two tasks have the same deadline, place larger manual tasks first and quick AI-generated tasks as short review blocks.

PRIORITY RULES:
- Critical: overdue, due today, or due tomorrow.
- High: due in 2-3 days.
- Medium: due later.
- Low: no clear deadline or small optional review task.

Return ONLY valid JSON.
No markdown.
No explanation outside JSON.

Format exactly like this:

{
  \"plan\": [
    {
      \"priority\": \"High\",
      \"day\": \"Day 1\",
      \"task\": \"Task name here\",
      \"type\": \"Manual task\",
      \"deadline\": \"2026-06-10\",
      \"time\": \"1.5 hours\",
      \"reason\": \"Closest deadline and larger task\"
    },
    {
      \"priority\": \"Medium\",
      \"day\": \"Day 1\",
      \"task\": \"Task name here\",
      \"type\": \"AI-generated material task\",
      \"deadline\": \"2026-06-10\",
      \"time\": \"10 minutes\",
      \"reason\": \"Quick review task from uploaded material\"
    }
  ]
}
";

            $apiKey = $_ENV["OPENAI_API_KEY"] ?? null;

            if (!$apiKey) {
                $ai_response = "Error: API key not found in .env";
            } else {
                $ch = curl_init();

                curl_setopt_array($ch, [
                    CURLOPT_URL => "https://api.openai.com/v1/chat/completions",
                    CURLOPT_RETURNTRANSFER => true,
                    CURLOPT_POST => true,
                    CURLOPT_HTTPHEADER => [
                        "Content-Type: application/json",
                        "Authorization: Bearer " . $apiKey
                    ],
                    CURLOPT_POSTFIELDS => json_encode([
                        "model" => "gpt-4o-mini",
                        "temperature" => 0.15,
                        "messages" => [
                            [
                                "role" => "user",
                                "content" => $prompt
                            ]
                        ]
                    ]),
                    CURLOPT_TIMEOUT => 60
                ]);

                $apiResult = curl_exec($ch);
                $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);

                if (curl_errno($ch)) {
                    $ai_response = "cURL Error: " . curl_error($ch);
                } else {
                    $response = json_decode($apiResult, true);

                    if ($httpCode < 200 || $httpCode >= 300) {
                        $errorMessage = $response["error"]["message"] ?? $apiResult;
                        $ai_response = "OpenAI API Error: " . $errorMessage;
                    } else {
                        $raw = $response["choices"][0]["message"]["content"] ?? "";
                        $decoded = json_decode($raw, true);

                        if (
                            json_last_error() === JSON_ERROR_NONE &&
                            isset($decoded["plan"]) &&
                            is_array($decoded["plan"])
                        ) {
                            $plan = $decoded["plan"];

                            $cacheJson = json_encode(
                                ["plan" => $plan],
                                JSON_UNESCAPED_UNICODE
                            );

                            saveAiCache(
                                $conn,
                                $cacheKey,
                                "recovery_plan",
                                $user_id,
                                $cacheJson
                            );

                        } else {
                            $ai_response = "Invalid JSON returned from AI:\n" . $raw;
                        }
                    }
                }

                curl_close($ch);
            }
        }

        if (!empty($plan)) {
            $plan_json = json_encode($plan, JSON_UNESCAPED_UNICODE);

            $stmt = $conn->prepare("
                INSERT INTO recovery_plans (user_id, plan)
                VALUES (?, ?)
            ");

            if ($stmt) {
                $stmt->bind_param("is", $user_id, $plan_json);
                $stmt->execute();
            }
        }
    }
