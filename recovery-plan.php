function detectTaskType($task) {
    $materialId = $task["material_id"] ?? null;
    $description = strtolower($task["description"] ?? "");
    $notes = strtolower($task["notes"] ?? "");

    if (
        !empty($materialId) ||
        strpos($description, "ai-generated") !== false ||
        strpos($notes, "generated from study material") !== false
    ) {
        return "AI-generated material task";
    }

    return "Manual task";
}

function suggestedTimeRange($taskType, $title) {
    $titleLower = strtolower($title);

    if ($taskType === "AI-generated material task") {
        if (
            strpos($titleLower, "solve") !== false ||
            strpos($titleLower, "calculate") !== false ||
            strpos($titleLower, "analyze") !== false ||
            strpos($titleLower, "compare") !== false ||
            strpos($titleLower, "apply") !== false ||
            strpos($titleLower, "explain") !== false ||
            strpos($titleLower, "evaluate") !== false ||
            strpos($titleLower, "describe") !== false
        ) {
            return "10-15 minutes";
        }

        return "5-10 minutes";
    }

    return "1-2 hours";
}

function deadlineValue($task) {
    $deadline = $task["deadline"] ?? "";

    if ($deadline === "" || $deadline === "0000-00-00" || $deadline === null) {
        $deadline = $task["due_date"] ?? "";
    }

    if ($deadline === "" || $deadline === "0000-00-00" || $deadline === null) {
        return "No deadline";
    }

    return $deadline;
}

